  /// Get Roboto Mono for data display
  static TextStyle getDataTextStyle({
    required bool isLight,
    double fontSize = 14,
    FontWeight fontWeight = FontWeight.w400,
  }) {
    final Color textColor =
        isLight ? textHighEmphasisLight : textHighEmphasisDark;
    return GoogleFonts.robotoMono(
      fontSize: fontSize,
      fontWeight: fontWeight,
      color: textColor,
      letterSpacing: 0,
    );
  }

  /// Get semantic colors for current theme
  static Color getSemanticColor(String colorName, {required bool isLight}) {
    switch (colorName.toLowerCase()) {
      case 'success':
        return isLight ? successLight : successDark;
      case 'warning':
        return isLight ? warningLight : warningDark;
      case 'accent':
        return isLight ? accentLight : accentDark;
      case 'disabled':
        return isLight ? disabledLight : disabledDark;
      default:
        return isLight ? primaryLight : primaryDark;
    }
  }
